package javaDemo;

public class ForLoop {
	public static void main(String[] args) {
		
				
		for(int i=1; i<=50; i=i+1) {
			System.out.println(i);
			
		}
		
		for(int i=1; i<=25; i=i+1) {  							//i=i+1; or i++
			//System.out.println(i + " United States of America");
			 
			System.out.println(i+"  United States of America");
						 
		 }
	}


}

